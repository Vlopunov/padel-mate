export function FAQ({ onBack }) {
  const [openItems, setOpenItems] = useState({});
  const [searchQuery, setSearchQuery] = useState('');

  const toggleItem = (key) => {
    setOpenItems((prev) => ({ ...prev, [key]: !prev[key] }));
  };

  const filteredSections = searchQuery.trim()
    ? FAQ_DATA.map((section) => ({
        ...section,
        items: section.items.filter(
          (item) =>
            item.q.toLowerCase().includes(searchQuery.toLowerCase()) ||
            item.a.toLowerCase().includes(searchQuery.toLowerCase())
        ),
      })).filter((section) => section.items.length > 0)
    : FAQ_DATA;

  return (
    <div style={{ paddingBottom: 20 }}>
      {/* Header */}
      <div style={{ display: 'flex', alignItems: 'center', gap: 12, marginBottom: 16 }}>
        <button
          onClick={onBack}
          style={{
            background: 'none',
            border: 'none',
            color: COLORS.accent,
            fontSize: 20,
            cursor: 'pointer',
            padding: 4,
          }}
        >
          ‚Üê
        </button>
        <h2 style={{ fontSize: 20, fontWeight: 700, color: COLORS.text, margin: 0 }}>‚ùì FAQ</h2>
      </div>

      {/* Search */}
      <div style={{ marginBottom: 16 }}>
        <input
          type="text"
          placeholder="–ü–æ–∏—Å–∫ –ø–æ FAQ..."
          value={searchQuery}
          onChange={(e) => setSearchQuery(e.target.value)}
          style={{
            width: '100%',
            padding: '10px 14px',
            borderRadius: 10,
            border: `1px solid ${COLORS.border}`,
            background: COLORS.card,
            color: COLORS.text,
            fontSize: 14,
            outline: 'none',
            boxSizing: 'border-box',
          }}
        />
      </div>

      {/* Sections */}
      {filteredSections.length === 0 && (
        <p style={{ textAlign: 'center', color: COLORS.textDim, fontSize: 14, marginTop: 40 }}>
          –ù–∏—á–µ–≥–æ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ
        </p>
      )}

      {filteredSections.map((section, si) => (
        <Card key={si} style={{ marginBottom: 12 }}>
          <h4 style={{ fontSize: 15, fontWeight: 700, color: COLORS.text, marginBottom: 4 }}>
            {section.section}
          </h4>
          {section.items.map((item, qi) => {
            const key = `${si}_${qi}`;
            return (
              <FAQItem
                key={key}
                q={item.q}
                a={item.a}
                isOpen={!!openItems[key] || !!searchQuery.trim()}
                onToggle={() => toggleItem(key)}
              />
            );
          })}
        </Card>
      ))}
    </div>
  );
}
