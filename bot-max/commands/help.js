import { Keyboard } from "@maxhub/max-bot-api";

export async function helpCommand(ctx, miniAppUrl) {
  const keyboard = Keyboard.inlineKeyboard([
    [Keyboard.button.link("🎾 Открыть Padel GO", miniAppUrl)],
  ]);

  await ctx.reply(
    `❓ <b>Помощь Padel GO</b>\n\n` +
      `<b>Профиль и рейтинг:</b>\n` +
      `/me — Мой профиль, рейтинг и статистика\n` +
      `/top — Топ-10 игроков\n\n` +
      `<b>Матчи:</b>\n` +
      `/matches — Мои предстоящие матчи\n` +
      `/find — Найти доступный матч\n` +
      `/create — Создать новый матч\n` +
      `/cancel — Выйти из матча\n\n` +
      `<b>Тренировки:</b>\n` +
      `/schedule — Расписание тренировок\n` +
      `/students — Мои ученики (для тренеров)\n\n` +
      `<b>Прочее:</b>\n` +
      `/faq — Частые вопросы\n` +
      `/stats — Статистика платформы (админ)\n` +
      `/help — Эта справка\n\n` +
      `<b>Как это работает:</b>\n` +
      `1. Откройте приложение через кнопку ниже\n` +
      `2. Пройдите короткий онбординг\n` +
      `3. Создавайте или ищите матчи\n` +
      `4. Записывайте счёт после игры\n` +
      `5. Следите за рейтингом и достижениями\n\n` +
      `Бот также присылает:\n` +
      `⏰ Напоминания о матчах\n` +
      `🎉 Уведомления когда матч собран\n` +
      `📈 Изменения рейтинга\n` +
      `🏅 Новые достижения\n` +
      `📋 Еженедельные сводки\n\n` +
      `При вопросах пишите в общий чат сообщества!`,
    { format: "html", attachments: [keyboard] }
  );
}
